# Ask the user to enter mathematics score
mathematics = float(input('Enter mathematics: '))

# Ask the user to enter chemistry score
chemistry = float(input('Enter chemistry: '))

# Ask the user to enter physics score
physics = float(input('Enter physics: '))

# Calculate the average of the three scores
average = (mathematics + chemistry + physics) / 3

# Print the result
print('The average is', average)
