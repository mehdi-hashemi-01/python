# Ask the user to enter the width and convert it to integer
width = int(input("Enter the width: "))

# Ask the user to enter the height and convert it to integer
height = int(input("Enter the height: "))

# Calculate the area (width × height)
area = width * height

# Print the result
print("The area is:", area)
